<?php

namespace SchedulingBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * ScheduleBlock
 *
 * @ORM\Table(name="schedule_block")
 * @ORM\Entity(repositoryClass="SchedulingBundle\Repository\ScheduleBlockRepository")
 */
class ScheduleBlock
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="text", type="string", length=255)
     */
    private $name;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set text
     *
     * @param string $text
     *
     * @return ScheduleBlock
     */
    public function setText($text)
    {
        $this->text = $text;

        return $this;
    }

    /**
     * Get text
     *
     * @return string
     */
    public function getText()
    {
        return $this->text;
    }
}

